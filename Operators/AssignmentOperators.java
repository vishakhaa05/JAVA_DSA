package Operators;

public class AssignmentOperators {
    public static void main(String[] args) {
        int num = 10;
        num += 5; // num = num + 5
        System.out.println("After += : " + num);
        num -= 3;
        System.out.println("After -= : " + num);
        num *= 2;
        System.out.println("After *= : " + num);
        num /= 4;
        System.out.println("After /= : " + num);
    }
}

